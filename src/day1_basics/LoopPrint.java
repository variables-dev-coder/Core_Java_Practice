package day1_basics;


// Write a Java program to print numbers from 1 to 100 using a for loop.

public class LoopPrint {
    public static void main(String[] args) {
        System.out.println("Number from 1 to 100: ");

        for(int i = 1; i<=100; i++){
            System.out.println(i);
        }
    }
}
