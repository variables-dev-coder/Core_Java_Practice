package revisaion_Core_Oop;


// D. Enhanced for loop (for-each loop)
//Used for arrays or collections.

public class ForEachExample {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};
        for (int num : arr) {
            System.out.println(num);
        }
    }
}


