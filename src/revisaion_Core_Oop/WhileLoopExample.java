package revisaion_Core_Oop;


// B. while loop
//When the number of iterations is not known but depends on a condition.

public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println("Count: " + i);
            i++;
        }
    }
}
